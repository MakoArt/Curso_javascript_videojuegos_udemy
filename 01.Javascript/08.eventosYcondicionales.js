document.addEventListener('keydown',(e)=>{

   if(e.key==='d'){
      console.log('moviendo hacia la derecha')
   }

   if(e.key==='s'){
       console.log('moviendo hacia abajo')
   }

   if(e.key==='w'){
       console.log('moviendo hacia arriba')
   }

   if(e.key==='a'){
       console.log('moviendo hacia la izquierda')
   }

  
})



